package build

import (
	"bytes"
	"fmt"
	"io/fs"
	"os"
	"path/filepath"
	"sort"
	"strings"
)

// Marker is the file Write drops in every output directory it owns. Its
// presence is what lets a later Write treat the directory's visible contents
// as disposable; a non-empty directory without it is refused, so a
// misconfigured output path can never wipe user files. Dot-prefixed, so the
// module walker never reads it back as input.
const Marker = ".kastorbuild"

const markerBody = "Generated by kastor build. Do not edit files in this directory.\n"

// Write syncs files into the output directory dir: changed files are
// written, unchanged files are left untouched (mtimes preserved), and
// visible files from a previous build that are no longer generated are
// removed, along with directories that emptied. Hidden (dot-prefixed)
// entries belong to the user and are never written, read, or removed.
// The directory is created if missing.
func Write(dir string, files []File) error {
	for _, f := range files {
		if err := f.validate(); err != nil {
			return fmt.Errorf("writing %s: %w", dir, err)
		}
	}

	if err := claim(dir); err != nil {
		return err
	}

	keep := map[string]bool{}
	for _, f := range files {
		rel := filepath.FromSlash(f.Path)
		keep[rel] = true
		if err := writeFile(filepath.Join(dir, rel), f.Data); err != nil {
			return fmt.Errorf("writing %s: %w", dir, err)
		}
	}

	if err := removeStale(dir, keep); err != nil {
		return fmt.Errorf("cleaning %s: %w", dir, err)
	}
	return nil
}

// claim makes dir safe to sync into: it creates the directory if needed and
// ensures the marker exists. A non-empty directory that has no marker was
// not produced by kastor build and is refused.
func claim(dir string) error {
	if err := os.MkdirAll(dir, 0o755); err != nil {
		return fmt.Errorf("creating output directory: %w", err)
	}

	markerPath := filepath.Join(dir, Marker)
	if _, err := os.Stat(markerPath); err == nil {
		return nil
	}

	entries, err := os.ReadDir(dir)
	if err != nil {
		return fmt.Errorf("reading output directory: %w", err)
	}
	if len(entries) > 0 {
		return fmt.Errorf("output directory %s is not empty and has no %s marker; refusing to overwrite it — remove the directory or point the target elsewhere", dir, Marker)
	}
	return os.WriteFile(markerPath, []byte(markerBody), 0o644)
}

// writeFile writes data to path, creating parent directories. An existing
// file with identical content is left untouched so file watchers and build
// caches downstream of the output directory only fire on real changes.
func writeFile(path string, data []byte) error {
	if existing, err := os.ReadFile(path); err == nil && bytes.Equal(existing, data) {
		return nil
	}
	if err := os.MkdirAll(filepath.Dir(path), 0o755); err != nil {
		return err
	}
	return os.WriteFile(path, data, 0o644)
}

// removeStale deletes visible files under dir that are not in keep (paths
// relative to dir, in OS form), then prunes directories that emptied.
// Hidden entries and everything beneath them are skipped: they are the
// user's, and a directory holding one never counts as empty.
func removeStale(dir string, keep map[string]bool) error {
	var dirs []string
	err := filepath.WalkDir(dir, func(path string, d fs.DirEntry, err error) error {
		if err != nil {
			return err
		}
		if strings.HasPrefix(d.Name(), ".") && path != dir {
			if d.IsDir() {
				return filepath.SkipDir
			}
			return nil
		}
		if d.IsDir() {
			if path != dir {
				dirs = append(dirs, path)
			}
			return nil
		}
		rel, err := filepath.Rel(dir, path)
		if err != nil {
			return err
		}
		if !keep[rel] {
			return os.Remove(path)
		}
		return nil
	})
	if err != nil {
		return err
	}

	// Children sort after their parent, so deleting in reverse order prunes
	// bottom-up. Remove fails harmlessly on directories that still hold
	// files (including hidden ones).
	sort.Sort(sort.Reverse(sort.StringSlice(dirs)))
	for _, d := range dirs {
		_ = os.Remove(d)
	}
	return nil
}
